    // === API ===
    let currentLang = detectLanguage();
    document.documentElement.lang = currentLang;

    function t(key, params = {}) {
        const parts = key.split('.');
        let val = TRANSLATIONS[currentLang];
        for (const p of parts) {
            if (val && typeof val === 'object') val = val[p];
            else { val = null; break; }
        }
        if (val == null) {
            // Fallback do EN
            let fbVal = TRANSLATIONS[DEFAULT];
            for (const p of parts) {
                if (fbVal && typeof fbVal === 'object') fbVal = fbVal[p];
                else { fbVal = null; break; }
            }
            val = fbVal || key;
        }
        if (typeof val === 'string' && Object.keys(params).length) {
            Object.entries(params).forEach(([k, v]) => {
                val = val.replace(new RegExp('\\{' + k + '\\}', 'g'), v);
            });
        }
        return val;
    }

    function setLanguage(lang) {
        if (!SUPPORTED.includes(lang)) return;
        currentLang = lang;
        localStorage.setItem('intake_lang', lang);
        document.documentElement.lang = lang;
        document.dispatchEvent(new CustomEvent('intake-lang-changed', { detail: { lang } }));
    }

    function getCurrentLanguage() { return currentLang; }
    function getSupportedLanguages() { return SUPPORTED.map(c => ({ code: c, name: LANG_NAMES[c], flag: LANG_FLAGS[c] })); }

    window.intakeI18n = { t, setLanguage, getCurrentLanguage, getSupportedLanguages, SUPPORTED, LANG_NAMES, LANG_FLAGS };
